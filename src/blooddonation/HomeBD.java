    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
     con=DBSqlite.ConnectDB();
    String name,bloodgroup,phne,email;
    name = jTextField1.getText();
    phne = jPhone.getText();
    bloodgroup = jBloodGroup.getText();
    email = jEmail.getText();

    if(name.equals("") || phne.equals("") || bloodgroup.equals("") || email.equals("") )
    {
        jLabel4.setText("Fields are empty");
        
    }
    else if(!jCheckBox3.isSelected())
    {
        JOptionPane.showMessageDialog(null, "Click on Agree all terms and Condition");
    }
    else{
     String sql = "INSERT INTO AddBloodDonor(Name , Phone, Email , BloodGroup) VALUES(?,?,?,?)";
          
        try{
            pst = con.prepareStatement(sql);
            pst.setString(1, jTextField1.getText());
            pst.setString(2, jPhone.getText());
            pst.setString(3, jEmail.getText());
            pst.setString(4, jBloodGroup.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Added Successfully");
            jTextField1.setText("All Capital Latter");
            jBloodGroup.setText("All Capital Latter");
            jPhone.setText("");
            jEmail.setText("");
        }
        catch(Exception e){
         JOptionPane.showMessageDialog(null, "Value not insert  " + e.getMessage());
        }
    } 
    }//GEN-LAST:event_btnAddActionPerformed

